#자기소개 프로그매
# 이름, 나이, 성별, 키
# 숫자는 +가 되지 않으므로 ,로 표시한다.

이름 = '백미르'      # 글자
나이 = 23            #숫자
성별 = '남자'
키 = "180.5CM"         # 소수점 있는 숫자
거주지 = "인천광역시 서구"

print('안녕하세요 제 이름은',이름,'입니다.')
print('나이는',나이,'이고 성별은',성별,'입니다.')
print('제 키는',키,'입니다.')
print('제가 살고 있는 지역은',거주지, '입니다.')

#127-4 
print(127-4)   #123
print('127-4')  # 127-4

